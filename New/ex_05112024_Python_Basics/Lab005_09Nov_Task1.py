# Take 3 inputs from user and perform add, sub, mul and div

print("Please enter 3 numbers")
num1 = int(input("num1: "))
num2 = int(input("num2: "))
num3 = int(input("num3: "))

add = num1 + num2 + num3
sub = num1 - num2 - num3
mul = num1 * num2
div = num1 / num2

print(f"The addition of all 3 numbers is: {add}")
print(f"The subtraction of all 3 numbers is: {sub}")
print(f"The multiplication of 2 numbers is: {mul}")
print(f"The division of 2 numbers is: {div}")
